// Prints the Five Times Table from 1 to 12

/*
Defining Table
Input                   Processing                  Output
Five Times Table        Loop to Calculate           Times Table Printout
to 12                   Times Table

*/

let times = 5;

for (let i = 1; i <= 12; i+=1) {
    console.log(times + ' x ' + i + ' = ' + times * i);
}

